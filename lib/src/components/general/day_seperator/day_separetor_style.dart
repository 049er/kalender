/// The [DaySeparatorStyle] class is used by the default [DaySeperator] widget.
class DaySeparatorStyle {
  const DaySeparatorStyle({
    this.thickness,
    this.color,
  });

  /// The thickness of the day seperator.
  final double? thickness;

  /// The day seperator color.
  final Color? color;
}
